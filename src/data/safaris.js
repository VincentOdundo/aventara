export function getSafarisByCountry(country) {
  return safariPackages.filter((s) => s.country === country);
}

export function getSafariBySlug(slug) {
  return safariPackages.find((s) => s.slug === slug);
}

export function getPrice(safari, season = null) {
  const s = season || getCurrentSeason();
  return safari.price[s];
}
